package carnegie.bioinfo.savi.filegeneration;


public class AcceptedListDB{

	private String pathwayId = "";
	private String pathwayCommonName = "";
	private String evidencdCode = "";
	private String ref = "";
	private String species = "";
	private String ciatations = "";
	private String taxonWarningFlagIfWithin = "";
	private String taxonWarningFlagIfOutsideOf = "";
	private String taxonWarningComment = "";
	
	public String getPathwayId() {
		return pathwayId;
	}
	public void setPathwayId(String pathwayId) {
		this.pathwayId = pathwayId;
	}
	public String getPathwayCommonName() {
		return pathwayCommonName;
	}
	public void setPathwayCommonName(String pathwayCommonName) {
		this.pathwayCommonName = pathwayCommonName;
	}
	public String getEvidencdCode() {
		return evidencdCode;
	}
	public void setEvidencdCode(String evidencdCode) {
		this.evidencdCode = evidencdCode;
	}
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String species) {
		this.species = species;
	}
	public String getCiatations() {
		return ciatations;
	}
	public void setCiatations(String ciatations) {
		this.ciatations = ciatations;
	}
	public String getTaxonWarningFlagIfWithin() {
		return taxonWarningFlagIfWithin;
	}
	public void setTaxonWarningFlagIfWithin(String taxonWarningFlagIfWithin) {
		this.taxonWarningFlagIfWithin = taxonWarningFlagIfWithin;
	}
	public String getTaxonWarningFlagIfOutsideOf() {
		return taxonWarningFlagIfOutsideOf;
	}
	public void setTaxonWarningFlagIfOutsideOf(String taxonWarningFlagIfOutsideOf) {
		this.taxonWarningFlagIfOutsideOf = taxonWarningFlagIfOutsideOf;
	}
	public String getTaxonWarningComment() {
		return taxonWarningComment;
	}
	public void setTaxonWarningComment(String taxonWarningComment) {
		this.taxonWarningComment = taxonWarningComment;
	}
	
	
}
